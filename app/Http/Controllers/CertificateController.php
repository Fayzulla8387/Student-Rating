    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $file = time().'.'.$request->image->extension();
        $path = "images/certificate/";
        $request->image->move(public_path($path), $file);
        $certificate = new Certificate();
        $certificate->user_id = Auth::id();
        $certificate->name = $request->name;
        $certificate->image = $path.$file;
        $certificate->date = $request->date;
        $certificate->save();
        return redirect()->back()->with('success', 'Certificate added successfully');
    }

